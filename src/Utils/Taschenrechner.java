package Utils;

public class Taschenrechner
{
    public static int Add(int a, int b)
    {
        return a+b;
    }

    static int  Sub(int a,int b)
    {
        return a-b;
    }

    static int Multiply(int a,int b)
    {
        return a*b;
    }

    static int Divide(double a,double b)
    {
        return (int)(a/b);
    }
}
